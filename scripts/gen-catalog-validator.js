#!/usr/bin/env node
/**
 * gen-catalog-validator.js
 *
 * 用 ajv 的 standalone 模式，把 shared/catalog.schema.json 在「构建期」预编译为一份
 * 静态校验器源码（catalogValidate.generated.ts），供 backend 与 worker 运行时直接 import。
 *
 * 为什么需要这一步：
 *   ajv 的 compile() 在运行时通过 `new Function(...)` 动态生成校验函数。而 Cloudflare
 *   Workers / Pages 运行时禁止动态代码生成（eval / new Function），会直接抛出
 *   `EvalError: Code generation from strings disallowed`。所以在构建期预编译、运行时只
 *   调用普通函数即可规避。
 *
 * 输出：CommonJS 形式的 standalone 代码会被转换为 ESM 默认导出（并标注 `as any`，
 * 因为生成代码含 ajv 注入的 `.errors` / `.evaluated` 赋值，纯静态类型不便表达）。
 */
const fs = require('fs');
const path = require('path');
const { createRequire } = require('module');

const root = path.resolve(__dirname, '..');

// ajv 位于 backend / worker 的 node_modules（或 Docker 中扁平化的 root/node_modules）。
// 用 createRequire 以 ajv 所在包为基准来解析，避免 scripts/ 目录无法找到依赖。
const ajvBases = [
  path.join(root, 'backend', 'node_modules', 'ajv', 'package.json'),
  path.join(root, 'worker', 'node_modules', 'ajv', 'package.json'),
  path.join(root, 'node_modules', 'ajv', 'package.json'),
];
const ajvBase = ajvBases.find((p) => fs.existsSync(p));
if (!ajvBase) {
  console.error('[gen-catalog-validator] ERROR: ajv not found');
  process.exit(1);
}
const req = createRequire(ajvBase);
const Ajv2020 = req('ajv/dist/2020');
const addFormats = req('ajv-formats');
const addErrors = req('ajv-errors');
const standaloneCode = req('ajv/dist/standalone');

const schemaPath = path.join(root, 'shared', 'catalog.schema.json');
const schema = JSON.parse(fs.readFileSync(schemaPath, 'utf-8'));

const ajv = new Ajv2020({ allErrors: true, strict: false, code: { source: true } });
addFormats(ajv);
addErrors(ajv);
const validate = ajv.compile(schema);

// standaloneCode 产出 CommonJS 模块（module.exports = validateNN; ...）
const code = standaloneCode(ajv, validate);

// 提取根校验函数名：standalone 代码以 `module.exports = validateNN;...` 导出根函数，
// 不能取首个 `function validateNN`（那可能是嵌套的子 schema 校验器）。
const m = code.match(/module\.exports\s*=\s*(validate\d+)/);
const name = m ? m[1] : 'validate24';

// 去掉 CJS 导出语句，改为 ESM 默认导出
const cjsExport = new RegExp(`module\\.exports(\\s*\\.\\s*default)?\\s*=\\s*${name}\\s*;?`, 'g');
const body = code.replace(cjsExport, '');

const header =
  '// AUTO-GENERATED by scripts/gen-catalog-validator.js — DO NOT EDIT.\n' +
  '// 由 ajv standalone 预编译 catalog schema 生成的校验器；运行时不再调用 new Function，\n' +
  '// 以兼容 Cloudflare Workers / Pages（其运行时禁止动态代码生成）。\n' +
  '// @ts-nocheck\n';
const out = header + body + `\nexport default ${name} as any;\n`;

const targets = process.env.CATALOG_VALIDATOR_OUT
  ? [process.env.CATALOG_VALIDATOR_OUT]
  : [
      path.join(root, 'backend', 'src', 'services', 'catalogValidate.generated.ts'),
      path.join(root, 'worker', 'src', 'services', 'catalogValidate.generated.ts'),
    ];

let wrote = 0;
for (const t of targets) {
  fs.mkdirSync(path.dirname(t), { recursive: true });
  fs.writeFileSync(t, out, 'utf-8');
  wrote++;
  console.log(`[gen-catalog-validator] wrote ${path.relative(root, t)}`);
}
console.log(`[gen-catalog-validator] done (${wrote} file(s))`);
